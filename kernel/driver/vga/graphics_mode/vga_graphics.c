bool write_registers(uint8_t *registers) {

	outb(VGA_MISC_REGISTER, *registers);
	registers++;

	for (uint8_t i = 0; i < 5; i++) {
		outb(VGA_SEQUENCER_INDEX, i);
		outb(VGA_SEQUENCER_DATA, *registers);
		registers++;
	}

	outb(VGA_CRTC_INDEX_PORT, 0x03);
	outb(VGA_CRTC_DATA_PORT, (inb(VGA_CRTC_INDEX_PORT) | 0x80));
	outb(VGA_CRTC_INDEX_PORT, 0x11);
	outb(VGA_CRTC_DATA_PORT, (inb(VGA_CRTC_DATA_PORT) & ~0x80));
	
	registers[0x03] |= 0x80;
	registers[0x11] &= ~0x80;

	for (uint8_t i = 0; i < 25; i++) {
		outb(VGA_CRTC_INDEX_PORT, i);
		outb(VGA_CRTC_DATA_PORT, *registers);
		registers++;
	}

	for (uint8_t i = 0; i < 9; i++) {
		outb(VGA_GRAPHICS_CONTROLLER_INDEX, i);
		outb(VGA_GRAPHICS_CONTROLLER_DATA, *registers);
		registers++;
	}

	for (uint8_t i = 0; i < 21; i++) {
		(void)inb(VGA_ATTRIBUTES_CONTROLLER_RESET);
		outb(VGA_ATTRIBUTES_CONTROLLER_INDEX, i);
		outb(VGA_ATTRIBUTES_CONTROLLER_WRITE, *registers);
		registers++;
	}

	(void)inb(VGA_ATTRIBUTES_CONTROLLER_RESET);
	outb(VGA_ATTRIBUTES_CONTROLLER_INDEX, 0x20);

	return true;
}

void putpixel(uint16_t x, uint16_t y, uint8_t color) {
    
	uint16_t offset = 320 * y + x;
    uint8_t *location = (uint8_t *)VGA_GRAPHIC_ADDRESS;
	location[offset] = color;
}

void draw_rect(uint16_t width, uint16_t height, uint16_t x, uint16_t y, uint8_t color) {
    
	for (uint16_t i = 0; i < width + 1; i++) {
        for (uint16_t j = 0; j < height + 1; j++) {
            putpixel(x+i, y+j, color);
        }
    }
}

void draw_line(uint16_t width, uint16_t x, uint16_t y, uint8_t color) {
    
	for (uint16_t i = 0; i < width+x; i++) {
		putpixel(width+i, y, color);
	}
}

void draw_char(uint8_t character, uint16_t pos_x, uint16_t pos_y, uint8_t color) {

	int32_t mask[8] = {1, 2, 4, 8, 16, 32, 64, 128};

	uint8_t *glyph = g_8x16_font + (int32_t)character * 16;

	for (uint8_t cy = 0; cy < 16; cy++) {
		for (uint8_t cx = 0; cx < 8; cx++) {
			putpixel(glyph[cy]&mask[cx]?color:0, pos_x+cx, pos_y+cy-12);
		}
	}
}

void draw_string(char *string, uint16_t pos_x, uint16_t pos_y, uint8_t color) {
	
	for (uint16_t i = 0; i < strlen(string); i++) {
		draw_char(string[i], pos_x, pos_y, color);
	}
}
